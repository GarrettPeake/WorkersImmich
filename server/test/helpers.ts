import { env, createExecutionContext, waitOnExecutionContext } from 'cloudflare:test';
import { applyD1Migrations } from 'cloudflare:test';
import app from '../src/index';

/**
 * Make a request to the worker app.
 */
export async function request(path: string, options?: RequestInit): Promise<Response> {
  const url = new URL(path, 'http://localhost');
  const req = new Request(url.toString(), options);
  const ctx = createExecutionContext();
  const response = await app.fetch(req, env, ctx);
  await waitOnExecutionContext(ctx);
  return response;
}

/**
 * Make an authenticated request with a session token.
 */
export async function authRequest(path: string, token: string, options?: RequestInit): Promise<Response> {
  const headers = new Headers(options?.headers);
  headers.set('Authorization', `Bearer ${token}`);
  return request(path, { ...options, headers });
}

/**
 * Make an authenticated request with an API key.
 */
export async function apiKeyRequest(path: string, apiKey: string, options?: RequestInit): Promise<Response> {
  const headers = new Headers(options?.headers);
  headers.set('x-api-key', apiKey);
  return request(path, { ...options, headers });
}

/**
 * Initialize the database schema for testing.
 *
 * Uses the built-in `applyD1Migrations` from `@cloudflare/vitest-pool-workers`
 * which reads migrations passed via the `TEST_MIGRATIONS` binding in vitest.config.ts.
 */
export async function setupDatabase(): Promise<void> {
  await applyD1Migrations(env.DB, (env as any).TEST_MIGRATIONS);
}

/**
 * Create a test admin user via the admin-sign-up endpoint and return auth
 * credentials (access token + user ID).
 */
export async function createTestAdmin(): Promise<{ token: string; userId: string }> {
  // Sign up admin (first user)
  const signupRes = await request('/api/auth/admin-sign-up', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({
      email: 'admin@test.com',
      password: 'password123',
      name: 'Test Admin',
    }),
  });

  const signupData = (await signupRes.json()) as any;

  // Login to get token
  const loginRes = await request('/api/auth/login', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({
      email: 'admin@test.com',
      password: 'password123',
    }),
  });

  const loginData = (await loginRes.json()) as any;
  return {
    token: loginData.accessToken,
    userId: signupData.id || loginData.userId,
  };
}

